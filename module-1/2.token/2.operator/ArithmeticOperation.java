class ArithmeticOperation{
	public static void main(String[] args) {
	int i = 10;
	System.out.println("The value of i : " + i);

	int j = 5;
	System.out.println("The value of j : " + j);

	int a = i + j;
	System.out.println("The Addition of " + i + " and " + j + " : " + a);

	a = i - j;
	System.out.println("The Subtraction of " + i + " and " + j + " : " + a);

	a = i * j;
	System.out.println("The Multiplication of " + i + " and " + j + " : " + a);

	a = i % j;
	System.out.println("The Remainder of " + i + " and " + j + " : " + a);

	a = i / j;
	System.out.println("The Quotient of " + i + " and " + j + " : " + a);


	}
}