class CompundAssignmentOperation{
	public static void main(String args[]){
		int i = 10;
		System.out.println("The content of i: " + i);

		i+=25;
		System.out.println("The content of i after +25: " + i);

		i-=5;
		System.out.println("The content of i after -5: " + i);

		i*=6;
		System.out.println("The content of i after *6: " + i);

		i/=5;
		System.out.println("The content of i after /5: " + i);

		i%=2;
		System.out.println("The content of i after %2: " + i);
	}
}