class LogicalOperation{
	public static void main(String[] args) {
		System.out.println("Res of true&&true is: " + (true&&true));
		System.out.println("Res of true&&false is: " + (true&&false));
		System.out.println("Res of false&&false is: " + (false&&false));

		System.out.println("Res of true||true is: " + (true||true));
		System.out.println("Res of true||false is: " + (true||false));
		System.out.println("Res of false||false is: " + (false||false));

		System.out.println("Res of !true is: " + (!true));
	}
}